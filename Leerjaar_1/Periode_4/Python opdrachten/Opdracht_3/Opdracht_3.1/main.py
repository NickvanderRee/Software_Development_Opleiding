# Function: Checks if you are old enough to take driving lessons.
# Author: Nick van der Ree

leeftijd = int(input("Wat is je leeftijd? "))

if leeftijd >= 18:
    print("Gefeliciteerd! Je bent oud genoeg om rijlessen te nemen.")
else:
    print("Sorry, je bent nog niet oud genoeg om rijlessen te nemen.")